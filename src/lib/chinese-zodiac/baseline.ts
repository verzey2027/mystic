/**
 * Get baseline Chinese zodiac reading
 * This provides deterministic interpretation based on animal, element, and period
 * 
 * @param input - Chinese zodiac input parameters
 * @returns Baseline Chinese zodiac reading
 */
export function getBaselineChineseZodiacReading(input: ChineseZodiacInput): ChineseZodiacReading {
  const { birthYear, period, date } = input;
  
  // Calculate animal and element from birth year
  const animal = calculateChineseZodiac(birthYear);
  const element = calculateChineseElement(birthYear);
  
  // Get metadata
  const animalMeta = getAnimalMetadata(animal);
  const elementMeta = getElementMetadata(element);
  
  // Get fortune template for this animal
  const template = FORTUNE_TEMPLATES[animal];
  
  // Determine period key
  const periodKey = period === TimePeriod.DAILY ? 'daily' 
                  : period === TimePeriod.WEEKLY ? 'weekly' 
                  : 'monthly';
  
  // Calculate date range based on period
  const dateRange = calculateDateRange(date, period);
  
  // Combine lucky colors from both animal and element
  const luckyColors = [
    ...animalMeta.luckyColors,
    ...elementMeta.colors
  ].filter((color, index, self) => self.indexOf(color) === index); // Remove duplicates
  
  // Generate lucky numbers based on animal and element
  const luckyNumbers = generateLuckyNumbers(animal, element, date);
  
  return {
    animal,
    element,
    thaiName: animalMeta.thaiName,
    chineseName: animalMeta.chineseName,
    period,
    dateRange,
    fortune: {
      overall: template.overall[periodKey],
      career: template.career[periodKey],
      wealth: template.wealth[periodKey],
      health: template.health[periodKey],
      relationships: template.relationships[periodKey]
    },
    luckyColors: luckyColors.slice(0, 3), // Limit to 3 colors
    luckyNumbers: luckyNumbers,
    luckyDirections: animalMeta.luckyDirections,
    advice: template.advice
  };
}

/**
 * Calculate date range for the given period
 */
function calculateDateRange(date: Date, period: TimePeriod): { start: Date; end: Date } {
  const start = new Date(date);
  const end = new Date(date);
  
  if (period === TimePeriod.DAILY) {
    // Same day
    start.setHours(0, 0, 0, 0);
    end.setHours(23, 59, 59, 999);
  } else if (period === TimePeriod.WEEKLY) {
    // Monday to Sunday of current week
    const dayOfWeek = start.getDay();
    const diff = dayOfWeek === 0 ? -6 : 1 - dayOfWeek; // Adjust to Monday
    start.setDate(start.getDate() + diff);
    start.setHours(0, 0, 0, 0);
    
    end.setDate(start.getDate() + 6); // Sunday
    end.setHours(23, 59, 59, 999);
  } else {
    // Monthly: First to last day of month
    start.setDate(1);
    start.setHours(0, 0, 0, 0);
    
    end.setMonth(end.getMonth() + 1, 0); // Last day of month
    end.setHours(23, 59, 59, 999);
  }
  
  return { start, end };
}

/**
 * Generate lucky numbers based on animal, element, and date
 * Uses deterministic algorithm for consistency
 */
function generateLuckyNumbers(
  animal: ChineseZodiacAnimal, 
  element: ChineseElement, 
  date: Date
): number[] {
  // Get base lucky numbers from animal metadata
  const animalMeta = getAnimalMetadata(animal);
  const baseLuckyNumbers = [...animalMeta.luckyNumbers];
  
  // Add element-based number
  const elementNumbers: Record<ChineseElement, number> = {
    [ChineseElement.WOOD]: 3,
    [ChineseElement.FIRE]: 9,
    [ChineseElement.EARTH]: 5,
    [ChineseElement.METAL]: 7,
    [ChineseElement.WATER]: 1
  };
  
  baseLuckyNumbers.push(elementNumbers[element]);
  
  // Add date-based number (day of month)
  const dayNumber = date.getDate();
  if (dayNumber <= 9) {
    baseLuckyNumbers.push(dayNumber);
  } else {
    // Reduce to single digit
    const reduced = Math.floor(dayNumber / 10) + (dayNumber % 10);
    baseLuckyNumbers.push(reduced);
  }
  
  // Remove duplicates and sort
  const uniqueNumbers = [...new Set(baseLuckyNumbers)].sort((a, b) => a - b);
  
  // Return up to 5 lucky numbers
  return uniqueNumbers.slice(0, 5);
}

/**
 * Helper function to calculate Chinese zodiac from birth year
 * Re-exported from animals.ts for convenience
 */
function calculateChineseZodiac(birthYear: number): ChineseZodiacAnimal {
  const animalIndex = (birthYear - 4) % 12;
  const animals = [
    ChineseZodiacAnimal.RAT, ChineseZodiacAnimal.OX, ChineseZodiacAnimal.TIGER,
    ChineseZodiacAnimal.RABBIT, ChineseZodiacAnimal.DRAGON, ChineseZodiacAnimal.SNAKE,
    ChineseZodiacAnimal.HORSE, ChineseZodiacAnimal.GOAT, ChineseZodiacAnimal.MONKEY,
    ChineseZodiacAnimal.ROOSTER, ChineseZodiacAnimal.DOG, ChineseZodiacAnimal.PIG
  ];
  return animals[animalIndex];
}

/**
 * Helper function to calculate Chinese element from birth year
 * Re-exported from animals.ts for convenience
 */
function calculateChineseElement(birthYear: number): ChineseElement {
  const elementIndex = Math.floor(((birthYear - 4) % 10) / 2);
  const elements = [
    ChineseElement.WOOD, ChineseElement.FIRE, ChineseElement.EARTH,
    ChineseElement.METAL, ChineseElement.WATER
  ];
  return elements[elementIndex];
}
