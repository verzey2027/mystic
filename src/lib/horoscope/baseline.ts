/**
 * Lucky color mappings based on zodiac element
 */
const ELEMENT_COLORS: Record<string, string[]> = {
  fire: ['แดง', 'ส้ม', 'ทอง', 'เหลือง'],
  earth: ['เขียว', 'น้ำตาล', 'เบจ', 'ครีม'],
  air: ['ฟ้า', 'เหลืองอ่อน', 'ขาว', 'เทา'],
  water: ['น้ำเงิน', 'เขียวทะเล', 'ม่วง', 'เงิน']
};

/**
 * Generate seed from zodiac sign and date for deterministic randomization
 */
function generateSeed(zodiacSign: ZodiacSign, date: Date): number {
  const signIndex = Object.values(ZodiacSign).indexOf(zodiacSign);
  const dateValue = date.getFullYear() * 10000 + (date.getMonth() + 1) * 100 + date.getDate();
  return signIndex * 1000000 + dateValue;
}

/**
 * Seeded random number generator for deterministic results
 */
function seededRandom(seed: number): number {
  const x = Math.sin(seed++) * 10000;
  return x - Math.floor(x);
}

/**
 * Select item from array using seeded random
 */
function selectFromArray<T>(array: T[], seed: number): T {
  const index = Math.floor(seededRandom(seed) * array.length);
  return array[index];
}

/**
 * Generate lucky numbers based on seed
 * Returns 3-5 numbers in range 1-99
 */
function generateLuckyNumbers(seed: number): number[] {
  const count = 3 + Math.floor(seededRandom(seed) * 3); // 3-5 numbers
  const numbers: number[] = [];
  
  for (let i = 0; i < count; i++) {
    const num = 1 + Math.floor(seededRandom(seed + i + 1) * 99);
    if (!numbers.includes(num)) {
      numbers.push(num);
    }
  }
  
  return numbers.sort((a, b) => a - b);
}

/**
 * Generate lucky colors based on zodiac element and seed
 * Returns 2-3 colors
 */
function generateLuckyColors(zodiacSign: ZodiacSign, seed: number): string[] {
  const metadata = getZodiacMetadata(zodiacSign);
  const elementColors = ELEMENT_COLORS[metadata.element];
  
  const count = 2 + Math.floor(seededRandom(seed) * 2); // 2-3 colors
  const colors: string[] = [];
  
  for (let i = 0; i < count && i < elementColors.length; i++) {
    const colorIndex = Math.floor(seededRandom(seed + i + 10) * elementColors.length);
    const color = elementColors[colorIndex];
    if (!colors.includes(color)) {
      colors.push(color);
    }
  }
  
  return colors;
}

/**
 * Get baseline horoscope interpretation
 * Provides deterministic content based on zodiac sign and date
 * 
 * @param zodiacSign - The zodiac sign
 * @param date - Reference date for the reading
 * @returns Baseline horoscope content with all aspects
 * 
 * @example
 * const baseline = getBaselineHoroscope(ZodiacSign.ARIES, new Date());
 * console.log(baseline.love); // Deterministic love aspect
 */
export function getBaselineHoroscope(
  zodiacSign: ZodiacSign,
  date: Date
): {
  love: string;
  career: string;
  finance: string;
  health: string;
  advice: string;
  luckyNumbers: number[];
  luckyColors: string[];
} {
  const template = BASELINE_TEMPLATES[zodiacSign];
  const seed = generateSeed(zodiacSign, date);
  
  return {
    love: selectFromArray(template.love, seed),
    career: selectFromArray(template.career, seed + 1),
    finance: selectFromArray(template.finance, seed + 2),
    health: selectFromArray(template.health, seed + 3),
    advice: selectFromArray(template.advice, seed + 4),
    luckyNumbers: generateLuckyNumbers(seed + 5),
    luckyColors: generateLuckyColors(zodiacSign, seed + 6)
  };
}
